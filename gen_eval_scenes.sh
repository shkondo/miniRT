#!/usr/bin/env bash
# miniRT 評価用テストシーンの生成（動作確認手順.md 0 章から切り出し）
#
# 使い方: リポジトリ直下で
#   bash gen_eval_scenes.sh
# ./tmp/ に評価用シーン 13 個を生成する（tmp/ は .gitignore 済み）。
# ヒアドキュメントは bash 構文。fish に中身をコピペせず、必ず bash で実行すること。
set -eu

if [ ! -f Makefile ] || [ ! -d scenes ]; then
	echo 'Error: miniRT リポジトリ直下で実行してください' >&2
	exit 1
fi

mkdir -p ./tmp

# 基本形状 1: 原点の球
cat > ./tmp/sphere_origin.rt <<'EOF'
A 0.2 255,255,255
C 0,0,-15 0,0,1 70
L -10,10,-10 0.7 255,255,255
sp 0,0,0 8 255,80,80
EOF

# 基本形状 2: z=0 の平面（カメラ正面の壁）
cat > ./tmp/plane_z0.rt <<'EOF'
A 0.2 255,255,255
C 0,0,-15 0,0,1 70
L -10,10,-15 0.7 255,255,255
pl 0,0,0 0,0,-1 120,120,180
EOF

# 基本形状 3: y 軸方向に伸びる円柱
cat > ./tmp/cyl_y.rt <<'EOF'
A 0.2 255,255,255
C 0,0,-15 0,0,1 70
L -10,15,-10 0.7 255,255,255
cy 0,0,10 0,1,0 6 12 255,200,60
EOF

# 移動: 原点の球 + 直径(6)より大きく平行移動(x+10)した球
cat > ./tmp/two_spheres.rt <<'EOF'
A 0.2 255,255,255
C 0,0,-20 0,0,1 70
L -10,12,-15 0.7 255,255,255
sp 0,0,0 6 255,80,80
sp 10,0,0 6 80,160,255
EOF

# 回転: 上の円柱を z 軸まわりに 90°回転（軸 0,1,0 → 1,0,0）
cat > ./tmp/cyl_rot90.rt <<'EOF'
A 0.2 255,255,255
C 0,0,-15 0,0,1 70
L -10,15,-10 0.7 255,255,255
cy 0,0,10 1,0,0 6 12 255,200,60
EOF

# 複数オブジェクト: 交差する球と円柱
cat > ./tmp/intersecting.rt <<'EOF'
A 0.2 255,255,255
C 0,0,-18 0,0,1 70
L -12,14,-12 0.7 255,255,255
cy 0,-5,10 0,1,0 6 12 80,200,120
sp 4,2,8 6 255,80,80
EOF

# カメラ: 同一シーンをカメラ位置・向きだけ変えて 4 方向から見る
# (x 軸上 / y 軸上=真下向き / z 軸上 / 軸外ランダム)
cat > ./tmp/cam_x.rt <<'EOF'
C 20,0,0 -1,0,0 70
A 0.2 255,255,255
L -10,12,-10 0.7 255,255,255
sp 0,0,0 6 255,80,80
sp 5,3,2 3 80,160,255
cy -4,-3,3 0,1,0 3 6 80,200,120
EOF

cat > ./tmp/cam_y.rt <<'EOF'
C 0,20,0 0,-1,0 70
A 0.2 255,255,255
L -10,12,-10 0.7 255,255,255
sp 0,0,0 6 255,80,80
sp 5,3,2 3 80,160,255
cy -4,-3,3 0,1,0 3 6 80,200,120
EOF

cat > ./tmp/cam_z.rt <<'EOF'
C 0,0,-20 0,0,1 70
A 0.2 255,255,255
L -10,12,-10 0.7 255,255,255
sp 0,0,0 6 255,80,80
sp 5,3,2 3 80,160,255
cy -4,-3,3 0,1,0 3 6 80,200,120
EOF

cat > ./tmp/cam_random.rt <<'EOF'
C 12,9,-15 -0.566,-0.424,0.707 70
A 0.2 255,255,255
L -10,12,-10 0.7 255,255,255
sp 0,0,0 6 255,80,80
sp 5,3,2 3 80,160,255
cy -4,-3,3 0,1,0 3 6 80,200,120
EOF

# 明るさ 1: 原点の球をカメラ横のライトで側面から照らす
cat > ./tmp/lit_side.rt <<'EOF'
A 0.1 255,255,255
C 0,0,-15 0,0,1 70
L -15,0,-5 0.8 255,255,255
sp 0,0,0 8 255,80,80
EOF

# 明るさ 2: 平行移動した球を側面から照らす（ハイライトは移動後の位置基準）
cat > ./tmp/lit_side_moved.rt <<'EOF'
A 0.1 255,255,255
C 0,0,-15 0,0,1 70
L -15,0,-5 0.8 255,255,255
sp 6,2,3 8 255,80,80
EOF

# 影: 真上のライト + 球 + 平面、カメラは斜め横から
cat > ./tmp/shadow.rt <<'EOF'
A 0.15 255,255,255
C -14,6,-14 0.667,-0.333,0.667 70
L 0,15,0 0.8 255,255,255
pl 0,-4,0 0,1,0 130,130,130
sp 0,2,0 6 255,80,80
EOF

echo "OK: ./tmp/ に評価用シーンを生成しました"
ls ./tmp/*.rt
